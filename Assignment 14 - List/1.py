"""Write a Python script to create a list of first N natural numbers."""
print("--------------------------------------------------------------------------------------------------------------")
num = int(input("Enter a natural number :"))
i = 1
l =[]
while i < num:
    l.append(int(input()))
    i += 1
l.sort()
print("The N first natural number list =",l)
